/**
 * @description Handle the sidebase opening
 * @returns {null}
 */
function w3_open() {
  document.getElementById("mySidebar").style.display = "block";
}

/**
 * @description Handle the sidebase closing
 * @returns {null}
 */
function w3_close() {
  document.getElementById("mySidebar").style.display = "none";
}
